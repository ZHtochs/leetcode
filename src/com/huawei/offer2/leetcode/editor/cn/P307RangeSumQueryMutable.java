     class NumArray {
        private int[] nums;
        private int[] sums;
        private int size;

        public NumArray(int[] nums) {
            this.nums = nums;
            size = (int) Math.ceil(Math.sqrt(nums.length));
            int length = (int) Math.ceil(nums.length * 1.0 / size);
            sums = new int[length];
            for (int i = 0; i < sums.length; i++) {
                for (int j = i * size; j < (i + 1) * size && j < nums.length; j++) {
                    sums[i] = sums[i] + nums[j];
                }
            }
        }

        public void update(int index, int val) {
            int num = nums[index];
            int diff = val - num;
            nums[index] = val;
            int position = index / size;
            sums[position] += diff;

        }

        public int sumRange(int left, int right) {
            int b1 = left / size, i1 = left % size, b2 = right / size, i2 = right % size;
            if (b1 == b2) { // åŒºé—´ [left, right] åœ¨åŒä¸€å—ä¸­
                int sum = 0;
                for (int j = left; j <= right; j++) {
                    sum += nums[j];
                }
                return sum;
            }
            int sum1 = 0;
            for (int j = i1; j < size; j++) {
                sum1 += nums[b1 * size + j];
            }
            int sum2 = 0;
            for (int j = 0; j <= i2; j++) {
                sum2 += nums[b2 * size + j];
            }
            int sum3 = 0;
            for (int j = b1 + 1; j < b2; j++) {
                sum3 += sums[j];
            }
            return sum1 + sum2 + sum3;
        }
    }

