public class P414ThirdMaximumNumber {

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        val pri: PriorityQueue<Int> by lazy { PriorityQueue(3) }

        fun thirdMax(nums: IntArray): Int {
            for (i: Int in nums) {
                if (pri.contains(i)) {
                    continue
                }
                if (pri.size < 3) {
                    pri.add(i)
                    continue
                }
                if (pri.peek() < i) {
                    pri.poll()
                    pri.add(i)
                }
            }
            if (pri.size < 3) {
                while (pri.size != 1) {
                    pri.poll()
                }
            }
            return pri.peek()
        }
    }
