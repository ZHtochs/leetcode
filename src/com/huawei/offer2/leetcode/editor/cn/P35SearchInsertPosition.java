    class Solution {
        public int searchInsert(int[] nums, int target) {
            int i = 0;
            int j = nums.length - 1;
            while (true) {
                if (i > j) {
                    break;
                }
                int middle = (i + j) / 2;
                if (target > nums[middle]) {
                    i = middle + 1;
                } else if (target < nums[middle]) {
                    j = middle - 1;
                } else {
                    return middle;
                }
            }
            return i;
        }
    }
